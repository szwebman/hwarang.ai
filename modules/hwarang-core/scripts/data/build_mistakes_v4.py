CORRECTION_KEYWORDS = [
    "정정", "잘못", "빗나감", "빗나갔", "가정이 틀", "가정이 어긋",
    "다시", "어긋", "추정이 틀", "추정 빗나", "다른 가능성", "우회",
    "되돌", "수정", "음,", "잠시만요", "예상과 다", "회복", "재분석",
    "찾았", "원인",
]


def has_correction(messages):
    """대화 내 어딘가에 정정 키워드가 한 번이라도 있어야."""
    for msg in messages:
        if msg.get("role") == "assistant":
            c = msg.get("content", "")
            for kw in CORRECTION_KEYWORDS:
                if kw in c:
                    return True
    return False


def validate_all(samples):
    missing = 0
    for s in samples:
        if not has_correction(s["messages"]):
            missing += 1
    return missing


