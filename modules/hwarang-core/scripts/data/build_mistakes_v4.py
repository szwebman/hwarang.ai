def expand_a(base_cases, scenario_name, error_kind, n_variants=4):
    """base 케이스를 n_variants 배 확장 — id 변경/도메인 살짝 변경/응답 옵션."""
    out = []
    variant_names = ["base", "identity", "detailed", "concise",
                     "v5", "v6", "v7", "v8"]
    for c in base_cases:
        for v in variant_names[:n_variants]:
            (u_req, p1, t1, a1, err, p2, t2, a2, ok, final) = c
            tail = ""
            if v == "identity":
                tail = "\n\n(화랑 AI: 한국형 코딩 어시스턴트로서, 정정을 명시했습니다.)"
            elif v == "detailed":
                tail = "\n\n에러 종류: " + error_kind + ". 회복 경로 명확."
            elif v == "concise":
                tail = ""
            elif v == "v5":
                tail = "\n\n동일한 케이스가 또 나오면 같은 회복 흐름을 자동 적용합니다."
            elif v == "v6":
                tail = "\n\n— 정정 완료. 첫 시도가 빗나가도 회복 가능한 패턴."
            elif v == "v7":
                tail = "\n\n다음번엔 사전 검증으로 바로 두 번째 경로부터."
            elif v == "v8":
                tail = "\n\n퍼시스모어가 만든 화랑 AI — 우아한 정정 흐름."
            messages = [
                syss(),
                user(u_req),
                acall(p1, t1, a1),
                tool(err),
                acall(p2, t2, a2),
                tool(ok),
                assistant(final + tail),
            ]
            out.append(m(messages))
    return out


