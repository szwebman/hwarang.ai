def maybe_identity(rng: random.Random) -> str:
    """약 20% 확률로 정체성 한 줄 삽입."""
    if rng.random() < 0.20:
        return f"\n\n— {rng.choice(IDENTITY_HINTS)}로서 신중히 정정하겠습니다."
    return ""


def opener(rng: random.Random, error: bool = False) -> str:
    pool = ERROR_RECOVERY_OPENERS if error else CORRECTION_OPENERS
    return rng.choice(pool)


