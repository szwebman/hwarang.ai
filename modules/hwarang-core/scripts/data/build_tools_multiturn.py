"""화랑 AI Multi-turn Tool Calling 학습 데이터

문제: LoRA가 multi-turn 에서 첫 응답이 plain text 면 후속 응답도 plain text 만 반환.
해결: 4개 시나리오 (연속 tool / 탐색 시퀀스 / 회복 패턴 / 후속 약속+즉시 tool) 각 50샘플+.
"""
import json, os, logging, argparse
logging.basicConfig(level=logging.INFO, format="%(asctime)s [%(levelname)s] %(message)s")
logger = logging.getLogger(__name__)

TOOLS_DESC = """당신은 화랑 AI입니다. 퍼시스모어가 만든 한국형 코딩 어시스턴트입니다.
사용 가능한 도구가 있을 때 <tool_call> 태그로 호출합니다.

사용 가능한 도구:
- read_file(path: str): 파일 내용 읽기
- write_file(path: str, content: str): 파일 쓰기
- list_directory(path: str): 디렉토리 목록
- search_code(pattern: str, path: str): 코드 검색 (grep)
- glob_files(pattern: str): 파일명 패턴 검색
- edit_file(path: str, old: str, new: str): 파일 수정
- run_command(command: str): 쉘 명령 실행
- get_diagnostics(): 에디터 에러/경고 확인

도구 호출 형식:
<tool_call>
{"name": "도구명", "arguments": {"인자": "값"}}
</tool_call>

도구 결과 후에는 결과를 바탕으로 응답합니다. 도구가 불필요하면 직접 답변합니다."""

def m(messages): return {"messages": messages}
def sys(): return {"role": "system", "content": TOOLS_DESC}
def user(c): return {"role": "user", "content": c}
def assistant(c): return {"role": "assistant", "content": c}
def tool(c): return {"role": "tool", "content": c}

def tc(name, args):
    """tool_call 블록 생성. arguments 는 dict"""
    return f'<tool_call>\n{json.dumps({"name": name, "arguments": args}, ensure_ascii=False)}\n</tool_call>'

def acall(prefix, name, args, suffix=""):
    """assistant: prefix 텍스트 직후 tool_call. 약속+즉시 tool 패턴"""
    s = prefix.rstrip() + "\n\n" + tc(name, args)
    if suffix:
        s += "\n\n" + suffix
    return assistant(s)


