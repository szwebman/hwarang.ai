_CJK = re.compile(r"[一-鿿]")
_CYRILLIC = re.compile(r"[Ѐ-ӿ]")
_HIRAKATA = re.compile(r"[぀-ヿ]")
_VIET = re.compile(
    r"[Ạ-ỹ]|[ạáảãàặắằẳẵệếềểễỉịòóỏõộốồổỗợớờởỡụúủũựứừửữỳýỷỹ]"
)
_HALLUC = re.compile(
    r"\b(Multiplex|MTP|Quoll|Annexion|Observations|SemaConnect|Aquila|Mega Telemetry)\b"
)
_CODE_BLOCK = re.compile(r"```.*?```", re.DOTALL)
_INLINE_CODE = re.compile(r"`[^`]+`")
_EN_RUN = re.compile(r"(?:[A-Za-z][A-Za-z\-']*\s+){2,}[A-Za-z][A-Za-z\-']*")


def is_clean_korean(text: str) -> bool:
    """assistant 응답이 한국어 only 정책을 만족하는지 검사."""
    code_stripped = _CODE_BLOCK.sub("", text)
    code_stripped = _INLINE_CODE.sub("", code_stripped)
    if _CJK.search(code_stripped):
        return False
    if _CYRILLIC.search(code_stripped):
        return False
    if _HIRAKATA.search(code_stripped):
        return False
    if _VIET.search(code_stripped):
        return False
    if _HALLUC.search(text):
        return False
    if _EN_RUN.search(code_stripped):
        return False
    return True


def reason_dirty(text: str) -> str:
    code_stripped = _CODE_BLOCK.sub("", text)
    code_stripped = _INLINE_CODE.sub("", code_stripped)
    if _CJK.search(code_stripped):
        return "cjk"
    if _CYRILLIC.search(code_stripped):
        return "cyrillic"
    if _HIRAKATA.search(code_stripped):
        return "hirakata"
    if _VIET.search(code_stripped):
        return "viet"
    if _HALLUC.search(text):
        return "halluc"
    if _EN_RUN.search(code_stripped):
        return "en_run"
    return "ok"


