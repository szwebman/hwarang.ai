def build_sample(gen: Callable[[], tuple[str, str]], category: str, max_retries: int = 3):
    for attempt in range(max_retries):
        q, a = gen()
        # 사용자 메시지(q) 는 외국어 허용. 응답(a) 만 한국어 only 검사.
        if not is_clean_korean(a):
            continue
        return {
            "category": category,
            "messages": [
                {"role": "user", "content": q},
                {"role": "assistant", "content": a},
            ],
        }
    return None


def build_all(verbose: bool = False) -> tuple[list[dict], dict]:
    samples: list[dict] = []
    stats = {"total_target": 0, "ok": 0, "skipped": 0, "by_reason": {}, "by_cat": {}}
    for name, count, gen in CATEGORIES:
        stats["total_target"] += count
        cat_ok = 0
        cat_skip = 0
        for _ in range(count):
            sample = build_sample(gen, name)
            if sample is None:
                cat_skip += 1
                stats["skipped"] += 1
                q, a = gen()
                reason = reason_dirty(a)
                stats["by_reason"][reason] = stats["by_reason"].get(reason, 0) + 1
                continue
            samples.append(sample)
            cat_ok += 1
            stats["ok"] += 1
        stats["by_cat"][name] = {"ok": cat_ok, "skipped": cat_skip}
        if verbose:
            print(f"  {name}: ok={cat_ok}, skipped={cat_skip}")
    return samples, stats


def write_jsonl(path: str, samples: list[dict]):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        for s in samples:
            f.write(json.dumps(s, ensure_ascii=False) + "\n")


def validate_file(path: str) -> int:
    dirty = 0
    total = 0
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            obj = json.loads(line)
            for m in obj.get("messages", []):
                if m.get("role") != "assistant":
                    continue
                total += 1
                if not is_clean_korean(m.get("content", "")):
                    dirty += 1
    print(f"[validate] total={total}, dirty={dirty}, ok={total - dirty}")
    return dirty


def main():
    parser = argparse.ArgumentParser(description="화랑 v8 code-switch 차단 SFT 데이터 빌더")
    parser.add_argument("--output", default="../../data/sft/code_switch_block_v8.jsonl")
    parser.add_argument("--validate", action="store_true",
                        help="기존 출력 파일을 재검증만 수행")
    parser.add_argument("-v", "--verbose", action="store_true")
    args = parser.parse_args()

    out = os.path.abspath(args.output)

    if args.validate:
        if not os.path.exists(out):
            print(f"파일 없음: {out}", file=sys.stderr)
            sys.exit(2)
        dirty = validate_file(out)
        sys.exit(0 if dirty == 0 else 1)

    samples, stats = build_all(verbose=args.verbose)
    write_jsonl(out, samples)
    print(f"[build_code_switch_block_v8] {len(samples)} samples → {out}")
    print(f"  target={stats['total_target']}, ok={stats['ok']}, skipped={stats['skipped']}")
    print(f"  by_cat={stats['by_cat']}")
    if stats["by_reason"]:
        print(f"  skip_reasons={stats['by_reason']}")


if __name__ == "__main__":
    main()
