def _build_l2(flow, id_q, id_a):
    msgs = [syss()]
    for step in flow:
        if len(step) == 2:
            u, a = step
            msgs.append(user(u))
            msgs.append(assistant(a))
        else:
            u, _none, t_name, t_args, t_res, t_final = step
            msgs.append(user(u))
            msgs.append(assistant(tc(t_name, t_args)))
            msgs.append(tool(t_res))
            msgs.append(assistant(t_final))
    msgs.append(user(id_q))
    msgs.append(assistant(id_a))
    return m(msgs)


